//----------------------------------
//機能：ゲームオーバー画像とSEの読み込み処理
//引数：なし
//戻値：成功or失敗
//----------------------------------
HRESULT GameOver::Load()
{
	if (FAILED(spt.Load("pictures\\太郎.jpg")))
	{
		return E_FAIL;
	}
	return S_OK;
}

//----------------------------------
//機能：更新処理
//引数：なし
//戻値：成功
//----------------------------------
HRESULT GameOver::Update()
{
	//スペースキー及びコントローラーのAを押すとプレイシーンへ移行する
	if (g_pInput->IsKeyTap(DIK_SPACE) /*|| g_pInput->IsPadButtonPush(XINPUT_GAMEPAD_A)*/)
	{
		g_gameScene = SC_TITLE;
	}
	return S_OK;
}

//----------------------------------
//機能：描画処理
//引数：なし
//戻値：成功
//----------------------------------
HRESULT GameOver::Render()
{
	//背景なので特に弄らず画面いっぱいに表示させる
	SpriteData data;
	//色と透明度
	data.color.a = 255;
	data.color.r = 255;
	data.color.g = 255;
	data.color.b = 255;
	spt.Draw(&data);

	return S_OK;
}